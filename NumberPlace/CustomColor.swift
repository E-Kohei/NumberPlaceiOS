import UIKit

class CustomColor {
    class var darkred: UIColor {
        get {
            return UIColor(red: 179.0/255, green: 0.0, blue: 32.0/255, alpha: 1.0)
        }
    }
    class var faintpink: UIColor {
        get {
            return UIColor(red: 1.0, green: 64.0/255, blue: 64.0/255, alpha: 180.0/255)
        }
    }
    class var seablue: UIColor {
        get {
            return UIColor(red: 0.0, green: 15.0/255, blue: 179.0/255, alpha: 1.0)
        }
    }
    class var faintblue: UIColor {
        get {
            return UIColor(red: 153.0/255, green: 183.0/255, blue: 1.0, alpha: 79.0/255)
        }
    }
    class var denseforest: UIColor {
        get {
            return UIColor(red: 0.0, green: 128.0/255, blue: 6.0/255, alpha: 1.0)
        }
    }
    class var vividgreen: UIColor {
        get {
            return UIColor(red: 134.0/255, green: 1.0, blue: 74.0/255, alpha: 200.0/255)
        }
    }
    class var faintgreen: UIColor {
        get {
            return UIColor(red: 119.0/255, green: 196.0/255, blue: 119.0/255, alpha: 1.0)
        }
    }
    class var ltgray: UIColor {
        get {
            return UIColor(red: 204.0/255, green: 204.0/255, blue: 204.0/255, alpha: 1.0)
        }
    }
}
